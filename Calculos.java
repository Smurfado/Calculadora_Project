
public class Calculos

{

    public int sum(int num1, int num2)

    {
        int soma = num1 + num2;
        return soma;
    }

    public int sub(int num1, int num2)

    {
        int soma = num1 - num2;
        return soma;
    }

    public int div(int num1, int num2)

    {
        int soma = num1 / num2;
        return soma;
    }

    public int multi(int num1, int num2)

    {
        int soma = num1 * num2;
        return soma;
    }

}